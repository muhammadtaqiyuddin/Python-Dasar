# ELIF = else if statement

nama = input("Nama kamu siapa? ")

if nama == "Taqi": # kondisi 1
	print("Hai anak ganteng!") # aksi true 1
elif nama == "Yuddin": # kondisi 2
	print("Hai anak Rajin!!") # aksi true 2
elif nama == "Mukhsin": # kondisi 3
	print("Hai anak ganteng!") # aksi true 3
else:
	print("gak kenal!!!") # aksi false

print("ini adalah akhir dari program")